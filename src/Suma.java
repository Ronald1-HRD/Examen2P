public class Suma {
    //Suma de dos enteros
    public int sumar(int a, int b) {
        return a + b;
    }
    //Suma de tres enteros

    public int sumar(int a, int b, int c) {
        return a + b + c;
    }
    //Suma de dos valores en metodo duble

    public double sumar(double a, double b) {
        return a + b;
    }
    //Suma de tres valores en metodo double

    public double sumar(double a, double b, double c) {
        return a + b + c;
    }
}
